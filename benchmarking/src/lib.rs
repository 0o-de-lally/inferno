//! Inferno Benchmarks
//!
//! This crate contains performance benchmarks for the Inferno distributed systems project.
//! The benchmarks are organized by concern:
//!
//! - `metrics` - Metrics collection performance
//! - `config` - Configuration parsing and validation
//! - `error` - Error handling performance
//! - `proxy_service` - Proxy service operations
//! - `concurrency` - Concurrent operations
//! - `memory` - Memory allocation efficiency
//! - `scalability` - Scalability and realistic workloads